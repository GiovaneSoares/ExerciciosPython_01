"""Exercício 07.

Dobro da Área do Quadrado
Crie um programa que calcule a área de um quadrado, e em seguida, mostre o dobro dessa
área para o usuário.
"""
area = float(input("Digite sua area e veja o dobro de seu quadrado: "))
soma = area * 2
print(f"A area do seu quadrado é de {soma:.2f}² e o dobro {soma * 2}²") 